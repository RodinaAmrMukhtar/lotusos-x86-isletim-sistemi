void gfx_fill_rect_direct(int x, int y, int w, int h, uint32_t rgb) {
  if (w <= 0 || h <= 0) return;
  int x0, y0, x1, y1;
  clip_bounds(x, y, w, h, &x0, &y0, &x1, &y1);
  for (int yy = y0; yy < y1; yy++) {
    for (int xx = x0; xx < x1; xx++) fb_putpx(xx, yy, rgb);
  }
}


static inline void blit32_span(uint32_t* dst, const uint32_t* src, int dwords) {
  if (dwords <= 0) return;
  __asm__ volatile("rep movsl"
                   : "+D"(dst), "+S"(src), "+c"(dwords)
                   :
                   : "memory");
}

static void present_rect_impl(int x0, int y0, int x1, int y1) {
  uint8_t* fb = (uint8_t*)(uintptr_t)g.fb_addr;

  if (x0 < 0) x0 = 0;
  if (y0 < 0) y0 = 0;
  if (x1 > (int)g.fb_w) x1 = (int)g.fb_w;
  if (y1 > (int)g.fb_h) y1 = (int)g.fb_h;
  if (x1 <= x0 || y1 <= y0) return;

  if (g.fb_bpp == 32) {
    for (int y = y0; y < y1; y++) {
      uint32_t* dst = (uint32_t*)(void*)(fb + (uint32_t)y * g.fb_pitch);
      const uint32_t* src = &g.bb[(uint32_t)y * g.fb_w];
      blit32_span(dst + (uint32_t)x0, src + (uint32_t)x0, (x1 - x0));
    }
    return;
  }

  if (g.fb_bpp == 24) {
    for (int y = y0; y < y1; y++) {
      uint8_t* dst = fb + (uint32_t)y * g.fb_pitch;
      uint32_t* src = &g.bb[(uint32_t)y * g.fb_w];
      for (int x = x0; x < x1; x++) {
        uint32_t rgb = src[(uint32_t)x];
        dst[(uint32_t)x * 3 + 0] = (uint8_t)(rgb & 0xFFu);
        dst[(uint32_t)x * 3 + 1] = (uint8_t)((rgb >> 8) & 0xFFu);
        dst[(uint32_t)x * 3 + 2] = (uint8_t)((rgb >> 16) & 0xFFu);
      }
    }
    return;
  }

  if (g.fb_bpp == 16) {
    for (int y = y0; y < y1; y++) {
      uint16_t* dst = (uint16_t*)(void*)(fb + (uint32_t)y * g.fb_pitch);
      uint32_t* src = &g.bb[(uint32_t)y * g.fb_w];
      for (int x = x0; x < x1; x++) dst[(uint32_t)x] = rgb_to_565(src[(uint32_t)x]);
    }
    return;
  }
}

void gfx_present(void) {
  present_rect_impl(0, 0, (int)g.fb_w, (int)g.fb_h);
}

void gfx_present_rect(int x, int y, int w, int h) {
  present_rect_impl(x, y, x + w, y + h);
}

