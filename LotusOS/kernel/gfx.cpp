void gfx_draw_round_rect(int x, int y, int w, int h, int r, uint32_t rgb) {
  if (w <= 0 || h <= 0) return;
  int x0, y0, x1, y1;
  clip_bounds(x - 1, y - 1, w + 2, h + 2, &x0, &y0, &x1, &y1);
  uint32_t v = rgb & 0x00FFFFFFu;

  for (int yy = y0; yy < y1; yy++) {
    for (int xx = x0; xx < x1; xx++) {
      int outer = point_in_round(xx, yy, x, y, w, h, r);
      int inner = point_in_round(xx, yy, x + 1, y + 1, w - 2, h - 2, r > 0 ? r - 1 : 0);
      if (!outer || inner) continue;
      if (!in_clip(xx, yy)) continue;
      g.bb[(uint32_t)yy * g.fb_w + (uint32_t)xx] = v;
    }
  }
}

void gfx_blit_argb(int x, int y, int w, int h, const uint32_t* src, int src_pitch) {
  if (!src || w <= 0 || h <= 0) return;
  if (src_pitch <= 0) src_pitch = w;

  for (int yy = 0; yy < h; yy++) {
    int dy = y + yy;
    if ((uint32_t)dy >= g.fb_h) continue;
    const uint32_t* row = src + yy * src_pitch;
    for (int xx = 0; xx < w; xx++) {
      int dx = x + xx;
      if ((uint32_t)dx >= g.fb_w) continue;
      if (!in_clip(dx, dy)) continue;

      uint32_t p = row[xx];
      uint8_t a = (uint8_t)((p >> 24) & 0xFFu);
      uint32_t rgb = (p & 0x00FFFFFFu);

      if (a == 255) bb_putpx(dx, dy, rgb);
      else if (a) bb_blendpx(dx, dy, rgb, a);
    }
  }
}

void gfx_blit_argb_key(int x, int y, int w, int h, const uint32_t* src, int src_pitch, uint32_t key_rgb) {
  if (!src || w <= 0 || h <= 0) return;
  if (src_pitch <= 0) src_pitch = w;
  key_rgb &= 0x00FFFFFFu;

  for (int yy = 0; yy < h; yy++) {
    int dy = y + yy;
    if ((uint32_t)dy >= g.fb_h) continue;
    const uint32_t* row = src + yy * src_pitch;
    for (int xx = 0; xx < w; xx++) {
      int dx = x + xx;
      if ((uint32_t)dx >= g.fb_w) continue;
      if (!in_clip(dx, dy)) continue;

      uint32_t p = row[xx];
      uint8_t a = (uint8_t)((p >> 24) & 0xFFu);
      uint32_t rgb = (p & 0x00FFFFFFu);
      if (rgb == key_rgb) continue;

      if (a == 255) bb_putpx(dx, dy, rgb);
      else if (a) bb_blendpx(dx, dy, rgb, a);
    }
  }
}

void gfx_draw_char(int x, int y, uint8_t ch, uint32_t fg, uint32_t bg) {
  uint32_t fh = g.font_h ? g.font_h : 16;

  if (!g.font_addr) {
    /* Built-in fallback font so text doesn't render as solid blocks when no BIOS font is provided. */
    uint32_t use_h = 16;
    for (uint32_t row = 0; row < use_h; row++) {
      uint8_t bits = g_builtin_font8x16[ch][row];
      for (uint32_t col = 0; col < 8; col++) {
        uint32_t on = (bits >> (7u - col)) & 1u;
        bb_putpx(x + (int)col, y + (int)row, on ? fg : bg);
      }
    }
    return;
  }

  const uint8_t* font = (const uint8_t*)(uintptr_t)g.font_addr;
  const uint8_t* glyph = font + (uint32_t)ch * fh;

  for (uint32_t row = 0; row < fh; row++) {
    uint8_t bits = glyph[row];
    for (uint32_t col = 0; col < 8; col++) {
      uint32_t on = (bits >> (7u - col)) & 1u;
      bb_putpx(x + (int)col, y + (int)row, on ? fg : bg);
    }
  }
}

void gfx_draw_text(int x, int y, const char* s, uint32_t fg, uint32_t bg) {
  if (!s) return;
  int cx = x;
  int cy = y;
  int fh = gfx_font_h();

  while (*s) {
    char c = *s++;
    if (c == '\n') { cx = x; cy += fh; continue; }
    if (c == '\r') { cx = x; continue; }
    gfx_draw_char(cx, cy, (uint8_t)c, fg, bg);
    cx += 8;
  }
}

